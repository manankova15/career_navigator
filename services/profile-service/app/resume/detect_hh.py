"""Score-based detection of hh.ru resume printouts."""

from __future__ import annotations

import re


_POSITIVE = [
    (r"\bhh\.ru\b", 0.12),
    (r"applicant/resumes/view", 0.14),
    (r"print=true", 0.08),
    (r"(?i)\bрезюме\b", 0.06),
    (r"Резюме обновлено", 0.08),
    (r"Опыт работы", 0.1),
    (r"Ключевые навыки", 0.08),
    (r"(?i)\bНавыки\b", 0.05),
    (r"Обо мне", 0.06),
    (r"Образование", 0.07),
    (r"Знание языков", 0.06),
    (r"Гражданство", 0.05),
    (r"Специализации", 0.06),
    (r"Занятость", 0.05),
    (r"График работы", 0.05),
    (r"Контакты", 0.05),
]

_NEGATIVE_KEYWORDS = [
    # Договоры / соглашения — только устойчивые формулировки
    r"\bдоговор\s+(?:№|N|Nº|No|номер)\b",
    r"\bдоговор\s+от\s+\d{1,2}[.\-/]\d{1,2}[.\-/]\d{2,4}\b",
    r"\bстороны\s+заключили\s+настоящий\s+договор\b",
    r"\bнастоящий\s+договор\s+(?:заключен|заключён|составлен)\b",
    r"\bпредмет\s+договора\b",
    r"\bусловия\s+настоящего\s+договора\b",
    r"\bсрок\s+действия\s+договора\b",
    r"\bрасторжение\s+договора\b",
    r"\bответственность\s+сторон\b",
    r"\bреквизиты\s+и\s+подписи\s+сторон\b",
    r"\bюридические\s+адреса\s+и\s+реквизиты\s+сторон\b",
    r"\bподписи\s+сторон\b",

    # Акты
    r"\bакт\s+(?:№|N|Nº|No|номер)\b",
    r"\bакт\s+выполненных\s+работ\b",
    r"\bакт\s+оказанных\s+услуг\b",
    r"\bакт\s+при[её]ма[-\s]?передачи\b",
    r"\bакт\s+сдачи[-\s]?при[её]мки\b",
    r"\bработы\s+выполнены\s+в\s+полном\s+объ[её]ме\b",
    r"\bзаказчик\s+претензий\s+не\s+имеет\b",

    # Счета / счета-фактуры / накладные
    r"\bсч[её]т\s+на\s+оплату\s+(?:№|N|Nº|No)?\b",
    r"\bсчет\s*[-\s]?фактура\s+(?:№|N|Nº|No)?\b",
    r"\bсч[её]т\s+действителен\s+в\s+течение\b",
    r"\bназначение\s+платежа\b",
    r"\bитого\s+к\s+оплате\b",
    r"\bв\s+том\s+числе\s+ндс\b",
    r"\bбез\s+ндс\b",
    r"\bплат[её]жное\s+поручение\b",
    r"\bтоварная\s+накладная\b",
    r"\bуниверсальный\s+передаточный\s+документ\b",
    r"\bупд\s+(?:№|N|Nº|No)\b",
    r"\bформа\s+торг[-\s]?12\b",

    # Справки / официальные документы
    r"\bсправка\s+о\s+доходах\b",
    r"\bсправка\s+2[-\s]?ндфл\b",
    r"\bсправка\s+по\s+форме\s+банка\b",
    r"\bсправка\s+с\s+места\s+работы\b",
    r"\bнастоящая\s+справка\s+выдана\b",
    r"\bвыдана\s+по\s+месту\s+требования\b",

    # Приказы / распоряжения
    r"\bприказ\s+(?:№|N|Nº|No|номер)\b",
    r"\bприказываю\b",
    r"\bоснование:\s*приказ\b",
    r"\bраспоряжение\s+(?:№|N|Nº|No|номер)\b",

    # Доверенности
    r"\bдоверенность\s+(?:№|N|Nº|No|номер)\b",
    r"\bнастоящей\s+доверенностью\b",
    r"\bуполномочивает\s+представлять\s+интересы\b",
    r"\bдоверенность\s+выдана\b",

    # Судебные / претензионные документы
    r"\bисковое\s+заявление\b",
    r"\bв\s+арбитражный\s+суд\b",
    r"\bв\s+районный\s+суд\b",
    r"\bпретензия\s+о\s+взыскании\b",
    r"\bдосудебная\s+претензия\b",
    r"\bисполнительный\s+лист\b",

    # Заявления / анкеты, не являющиеся резюме
    r"\bзаявление\s+на\s+увольнение\b",
    r"\bзаявление\s+о\s+при[её]ме\s+на\s+работу\b",
    r"\bзаявление\s+на\s+отпуск\b",
    r"\bанкета\s+соискателя\b",
    r"\bличная\s+карточка\s+работника\b",
    r"\bформа\s+т[-\s]?2\b",

    # Кадровые документы
    r"\bтрудовой\s+договор\s+(?:№|N|Nº|No)?\b",
    r"\bдолжностная\s+инструкция\b",
    r"\bштатное\s+расписание\b",
    r"\bтабель\s+уч[её]та\s+рабочего\s+времени\b",
    r"\bрасч[её]тный\s+листок\b",
    r"\bприказ\s+о\s+при[её]ме\s+на\s+работу\b",
    r"\bприказ\s+об\s+увольнении\b",

    # Банковские / бухгалтерские реквизиты
    r"\bинн\s*/\s*кпп\b",
    r"\bр/с\s*\d{10,}\b",
    r"\bк/с\s*\d{10,}\b",
    r"\bбик\s*\d{9}\b",
    r"\bокпо\b",
    r"\bогрн\b",
    r"\bюр(?:идический)?\s+адрес\b",
    r"\bбанковские\s+реквизиты\b",

    # Медицинские / учебные документы
    r"\bлисток\s+нетрудоспособности\b",
    r"\bмедицинское\s+заключение\b",
    r"\bвыписка\s+из\s+амбулаторной\s+карты\b",
    r"\bзач[её]тная\s+книжка\b",
    r"\bдиплом\s+о\s+профессиональной\s+переподготовке\b",
    r"\bакадемическая\s+справка\b",

    # Паспортные/личные документы
    r"\bпаспорт\s+гражданина\s+российской\s+федерации\b",
    r"\bсвидетельство\s+о\s+рождении\b",
    r"\bсвидетельство\s+о\s+заключении\s+брака\b",
    r"\bснилс\b",
]

_CONTRACT_LIKE = re.compile("|".join(f"(?:{p})" for p in _NEGATIVE_KEYWORDS), re.IGNORECASE)


def detect_hh_resume(normalized_text: str) -> tuple[bool, float, list[str]]:
    """
    Returns (is_hh_resume, score 0..1, warnings).
    Thresholds: >=0.75 valid, 0.45..0.75 suspicious, <0.45 not hh for autofill.
    """
    warnings: list[str] = []
    t = normalized_text or ""
    if len(t) < 200:
        warnings.append("Мало текста в документе — возможно скан без OCR.")
    score = 0.0
    for pattern, w in _POSITIVE:
        if re.search(pattern, t):
            score += w
    if _CONTRACT_LIKE.search(t):
        score -= 0.25
        warnings.append("Текст похож на договор или счёт — снижен балл.")
    if len(t) < 400:
        score -= 0.1
    score = max(0.0, min(1.0, score))
    is_hh = score >= 0.75
    if 0.45 <= score < 0.75:
        warnings.append("Файл похож на резюме hh.ru не полностью — проверьте данные вручную.")
    if score < 0.45:
        warnings.append("Низкая уверенность, что это резюме hh.ru — автозаполнение отключено.")
    return is_hh, score, warnings
